def choice_test():
    # Меню выбора теста
    test_num = input(
        "Выберите вариант теста от 1 до 4\n"
        "(1 - Политика, 2 - География, 3 - Литература, 4 - Закончить тест): ")
    # Проверяем ввод
    if test_num not in ["1", "2", "3","4"]:
        print("Некорректное значение")
        return choice_test()
    else:
        test_num = int(test_num)
    # Стартуем нужный тест или выходим
    if test_num == 1:
        return start_test_1()

    elif test_num == 2:
        return start_test_2()

    elif test_num == 3:
        return start_test_3()

    elif test_num == 4:
        return None


