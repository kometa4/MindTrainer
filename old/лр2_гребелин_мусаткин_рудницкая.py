def calculate_answers(result_test, test):
    # Превращаем словари в списки ответов
    result_answers = list(result_test.values())
    correct_answers = list(test.values())

    s = 0
    # Считаем совпадения по позициям
    for i in range(3):
        if result_answers[i] == correct_answers[i]:
            s += 1

    return s

def choice_answer(question, variantes):
    # Показываем вопрос и варианты
    print(question)
    for i in range(len(variantes)):
        print(f'{i + 1} - {variantes[i]}')
    # Читаем выбор пользователя
    answer = input("Выберите ответ: ")
    # Простая проверка корректности
    if answer not in ["1", "2", "3"]:
        print("Некорректный ответ")
        # Рекурсия: спрашиваем ещё раз
        return choice_answer(question, variantes)

    else:
        return variantes[int(answer) - 1] # Возвращаем выбранный вариант как текст


